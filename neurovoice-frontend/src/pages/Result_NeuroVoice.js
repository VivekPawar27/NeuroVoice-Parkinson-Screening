import { useLocation, useNavigate } from "react-router-dom";
import { useEffect } from "react";
import Header from "../components/Header";
import {
  LineChart, Line,
  BarChart, Bar,
  PieChart, Pie, Cell,
  ScatterChart, Scatter,
  XAxis, YAxis, CartesianGrid, Tooltip, Legend, ResponsiveContainer
} from "recharts";

function Result() {
  const location = useLocation();
  const navigate = useNavigate();
  const data = location.state;

  // Handle missing or error data
  useEffect(() => {
    if (!data) {
      alert("No analysis results available. Please upload and analyze an audio file first.");
      navigate("/");
    }
  }, [data, navigate]);

  if (!data) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <p className="text-xl text-gray-600">Loading...</p>
      </div>
    );
  }

  if (data.error) {
    return (
      <div className="min-h-screen flex items-center justify-center px-4">
        <div className="bg-red-50 border-2 border-red-500 rounded-lg p-8 max-w-md w-full">
          <h2 className="text-2xl font-bold text-red-700 mb-4">Analysis Failed</h2>
          <p className="text-red-600 mb-6">{data.error}</p>
          <button
            onClick={() => navigate("/")}
            className="w-full px-4 py-2 bg-red-500 text-white rounded-lg hover:bg-red-600"
          >
            Try Again
          </button>
        </div>
      </div>
    );
  }

  const getRiskColor = (level) => {
    if (!level) return { bg: "bg-sky-50", border: "border-sky-500", text: "text-sky-700", badge: "bg-sky-100 text-sky-800" };
    
    if (level.includes("High")) {
      return { bg: "bg-red-50", border: "border-red-500", text: "text-red-700", badge: "bg-red-100 text-red-800" };
    } else if (level.includes("Moderate")) {
      return { bg: "bg-yellow-50", border: "border-yellow-500", text: "text-yellow-700", badge: "bg-yellow-100 text-yellow-800" };
    } else if (level.includes("Low")) {
      return { bg: "bg-green-50", border: "border-green-500", text: "text-green-700", badge: "bg-green-100 text-green-800" };
    }
    return { bg: "bg-sky-50", border: "border-sky-500", text: "text-sky-700", badge: "bg-sky-100 text-sky-800" };
  };

  const colors = getRiskColor(data?.risk_level);

  // Prepare segment visualization data
  const segmentData = data?.segment_predictions?.map((seg, idx) => ({
    name: `Segment ${seg.segment}`,
    probability: (seg.probability_parkinsons * 100).toFixed(1),
    status: seg.status,
    confidence: (seg.confidence * 100).toFixed(1)
  })) || [];

  // Aggregate statistics
  const aggStats = data?.aggregate_stats || {};

  const downloadReport = () => {
    if (!data) return;

    let reportContent = `NeuroVoice Parkinson's Screening Report (CNN-LSTM Model)
================================================================
Generated: ${new Date().toLocaleString()}

ANALYSIS RESULTS:
-----------------
Overall Status: ${data.status}
Risk Score: ${data.risk_score?.toFixed(2) || 'N/A'}%
Risk Level: ${data.risk_level}
Confidence: ${(data.confidence * 100)?.toFixed(2) || 'N/A'}%

AUDIO ANALYSIS:
---------------
Audio Duration: ${data.audio_duration?.toFixed(2) || 'N/A'} seconds
Segments Analyzed: ${data.segments_analyzed || 'N/A'}
Model Used: ${data.model_used || 'CNN-LSTM NeuroVoice'}

SEGMENT-BY-SEGMENT RESULTS:
---------------------------`;

    if (data.segment_predictions && data.segment_predictions.length > 0) {
      data.segment_predictions.forEach((seg) => {
        reportContent += `\nSegment ${seg.segment}:
  - Status: ${seg.status}
  - Probability of Parkinson's: ${(seg.probability_parkinsons * 100).toFixed(2)}%
  - Confidence: ${(seg.confidence * 100).toFixed(2)}%`;
      });
    }

    reportContent += `\n\nAGGREGATE STATISTICS:
---------------------
Mean Probability: ${(aggStats.mean_probability * 100)?.toFixed(2) || 'N/A'}%
Std Deviation: ${(aggStats.std_probability * 100)?.toFixed(2) || 'N/A'}%
Max Probability: ${(aggStats.max_probability * 100)?.toFixed(2) || 'N/A'}%
Min Probability: ${(aggStats.min_probability * 100)?.toFixed(2) || 'N/A'}%

CLINICAL INSIGHTS:
------------------`;

    if (data.insights && data.insights.length > 0) {
      data.insights.forEach((insight, idx) => {
        reportContent += `\n${idx + 1}. ${insight}`;
      });
    }

    reportContent += `\n\nEXPLANATION:
${data.explanation || 'N/A'}

IMPORTANT DISCLAIMER:
This screening tool is for informational purposes only.
The results should not be considered as a medical diagnosis.
Always consult a qualified healthcare professional for proper
medical evaluation and diagnosis.

Generated by NeuroVoice Parkinson's Detection System
    `;

    const element = document.createElement("a");
    element.setAttribute("href", "data:text/plain;charset=utf-8," + encodeURIComponent(reportContent));
    element.setAttribute("download", `NeuroVoice_Report_${new Date().getTime()}.txt`);
    element.style.display = "none";
    document.body.appendChild(element);
    element.click();
    document.body.removeChild(element);
  };

  return (
    <div 
      className="min-h-screen"
      style={{
        backgroundImage: "linear-gradient(135deg, #d0e7f9 0%, #e0f2fe 50%, #cffafe 100%)"
      }}
    >
      <Header />
      
      <div className="max-w-6xl mx-auto px-4 py-10">
        {/* Main Results Card */}
        <div className={`${colors.bg} border-2 ${colors.border} rounded-3xl p-8 mb-8 shadow-2xl`}>
          <div className="text-center mb-8">
            <h1 className="text-5xl font-bold mb-4">Screening Results</h1>
            <div className={`inline-block px-6 py-3 ${colors.badge} rounded-full text-2xl font-bold`}>
              {data.status}
            </div>
          </div>

          <div className="grid grid-cols-1 md:grid-cols-3 gap-6 mb-8">
            {/* Risk Score */}
            <div className="bg-white/80 rounded-2xl p-6 text-center shadow-md">
              <p className="text-gray-600 font-semibold mb-2">Risk Score</p>
              <p className={`text-5xl font-bold ${colors.text}`}>
                {data.risk_score?.toFixed(1) || '0'}%
              </p>
            </div>

            {/* Risk Level */}
            <div className="bg-white/80 rounded-2xl p-6 text-center shadow-md">
              <p className="text-gray-600 font-semibold mb-2">Risk Level</p>
              <p className={`text-3xl font-bold ${colors.text}`}>
                {data.risk_level || 'Unknown'}
              </p>
            </div>

            {/* Confidence */}
            <div className="bg-white/80 rounded-2xl p-6 text-center shadow-md">
              <p className="text-gray-600 font-semibold mb-2">Confidence</p>
              <p className={`text-4xl font-bold ${colors.text}`}>
                {(data.confidence * 100)?.toFixed(1) || '0'}%
              </p>
            </div>
          </div>

          {/* Explanation */}
          <div className="bg-white/80 rounded-lg p-6 border-l-4" style={{borderColor: colors.border.split('-')[1]}}>
            <p className="text-gray-800 text-lg leading-relaxed">
              {data.explanation || 'No explanation available'}
            </p>
          </div>
        </div>

        {/* Segment Analysis Section */}
        {data.segment_predictions && data.segment_predictions.length > 0 && (
          <div className="bg-white/80 backdrop-blur-md rounded-3xl p-8 mb-8 shadow-2xl border border-sky-200/50">
            <h2 className="text-3xl font-bold text-sky-900 mb-6">Segment-by-Segment Analysis</h2>
            <p className="text-gray-600 mb-6">
              The audio was analyzed in {data.segment_predictions.length} segments of approximately 3 seconds each.
            </p>

            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4 mb-6">
              {data.segment_predictions.map((seg, idx) => (
                <div key={idx} className="bg-gradient-to-br from-sky-50 to-cyan-50 rounded-xl p-4 border border-sky-200">
                  <div className="flex justify-between items-start mb-3">
                    <p className="font-bold text-sky-900">Segment {seg.segment}</p>
                    <span className={`px-3 py-1 rounded-full text-xs font-semibold ${
                      seg.probability_parkinsons >= 0.5 
                        ? 'bg-red-100 text-red-800' 
                        : 'bg-green-100 text-green-800'
                    }`}>
                      {seg.status}
                    </span>
                  </div>
                  <div className="space-y-2">
                    <div>
                      <p className="text-sm text-gray-600">PD Probability</p>
                      <p className="text-2xl font-bold text-sky-700">
                        {(seg.probability_parkinsons * 100).toFixed(1)}%
                      </p>
                    </div>
                    <div>
                      <p className="text-sm text-gray-600">Confidence</p>
                      <p className="text-lg font-semibold text-sky-600">
                        {(seg.confidence * 100).toFixed(1)}%
                      </p>
                    </div>
                  </div>
                </div>
              ))}
            </div>

            {/* Segment Chart */}
            <ResponsiveContainer width="100%" height={300}>
              <BarChart data={segmentData}>
                <CartesianGrid strokeDasharray="3 3" />
                <XAxis dataKey="name" />
                <YAxis label={{ value: 'Probability (%)', angle: -90, position: 'insideLeft' }} />
                <Tooltip formatter={(value) => `${value}%`} />
                <Legend />
                <Bar dataKey="probability" fill="#3b82f6" name="PD Probability (%)" />
              </BarChart>
            </ResponsiveContainer>
          </div>
        )}

        {/* Aggregate Statistics */}
        {aggStats && Object.keys(aggStats).length > 0 && (
          <div className="bg-white/80 backdrop-blur-md rounded-3xl p-8 mb-8 shadow-2xl border border-sky-200/50">
            <h2 className="text-3xl font-bold text-sky-900 mb-6">Aggregate Statistics</h2>
            
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-4">
              <div className="bg-gradient-to-br from-blue-50 to-cyan-50 rounded-xl p-4 border border-blue-200">
                <p className="text-sm text-gray-600 font-semibold mb-2">Mean Probability</p>
                <p className="text-3xl font-bold text-blue-700">
                  {(aggStats.mean_probability * 100).toFixed(1)}%
                </p>
              </div>
              
              <div className="bg-gradient-to-br from-purple-50 to-pink-50 rounded-xl p-4 border border-purple-200">
                <p className="text-sm text-gray-600 font-semibold mb-2">Std Deviation</p>
                <p className="text-3xl font-bold text-purple-700">
                  {(aggStats.std_probability * 100).toFixed(1)}%
                </p>
              </div>
              
              <div className="bg-gradient-to-br from-orange-50 to-red-50 rounded-xl p-4 border border-orange-200">
                <p className="text-sm text-gray-600 font-semibold mb-2">Max Probability</p>
                <p className="text-3xl font-bold text-orange-700">
                  {(aggStats.max_probability * 100).toFixed(1)}%
                </p>
              </div>
              
              <div className="bg-gradient-to-br from-green-50 to-emerald-50 rounded-xl p-4 border border-green-200">
                <p className="text-sm text-gray-600 font-semibold mb-2">Min Probability</p>
                <p className="text-3xl font-bold text-green-700">
                  {(aggStats.min_probability * 100).toFixed(1)}%
                </p>
              </div>
            </div>
          </div>
        )}

        {/* Clinical Insights */}
        {data.insights && data.insights.length > 0 && (
          <div className="bg-white/80 backdrop-blur-md rounded-3xl p-8 mb-8 shadow-2xl border border-sky-200/50">
            <h2 className="text-3xl font-bold text-sky-900 mb-6">Clinical Insights</h2>
            <div className="space-y-3">
              {data.insights.map((insight, idx) => (
                <div key={idx} className="flex items-start gap-4 bg-sky-50 p-4 rounded-lg border-l-4 border-sky-500">
                  <span className="text-2xl">💡</span>
                  <p className="text-gray-800 text-sm md:text-base">{insight}</p>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* Audio Details */}
        <div className="bg-white/80 backdrop-blur-md rounded-3xl p-8 mb-8 shadow-2xl border border-sky-200/50">
          <h2 className="text-3xl font-bold text-sky-900 mb-6">Audio Analysis Details</h2>
          
          <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
            <div className="bg-gradient-to-br from-sky-50 to-cyan-50 rounded-xl p-6 border border-sky-200">
              <p className="text-sm text-gray-600 font-semibold mb-2">Audio Duration</p>
              <p className="text-2xl font-bold text-sky-700">
                {data.audio_duration?.toFixed(2) || 'N/A'} sec
              </p>
            </div>
            
            <div className="bg-gradient-to-br from-cyan-50 to-teal-50 rounded-xl p-6 border border-cyan-200">
              <p className="text-sm text-gray-600 font-semibold mb-2">Segments Analyzed</p>
              <p className="text-2xl font-bold text-cyan-700">
                {data.segments_analyzed || 'N/A'}
              </p>
            </div>
            
            <div className="bg-gradient-to-br from-teal-50 to-green-50 rounded-xl p-6 border border-teal-200">
              <p className="text-sm text-gray-600 font-semibold mb-2">Model Used</p>
              <p className="text-lg font-bold text-teal-700">
                {data.model_used || 'Unknown'}
              </p>
            </div>
          </div>
        </div>

        {/* Action Buttons */}
        <div className="flex gap-4 justify-center mb-8 flex-wrap">
          <button
            onClick={downloadReport}
            className="px-8 py-4 bg-gradient-to-r from-blue-500 to-blue-600 hover:from-blue-600 hover:to-blue-700 text-white font-bold rounded-xl transition transform hover:scale-105 shadow-lg"
          >
            📥 Download Report
          </button>
          <button
            onClick={() => navigate("/")}
            className="px-8 py-4 bg-gradient-to-r from-gray-500 to-gray-600 hover:from-gray-600 hover:to-gray-700 text-white font-bold rounded-xl transition transform hover:scale-105 shadow-lg"
          >
            🏠 New Analysis
          </button>
        </div>

        {/* Disclaimer */}
        <div className="bg-yellow-50 border-2 border-yellow-500 rounded-2xl p-6 mb-8">
          <p className="font-bold text-yellow-900 text-lg mb-2">⚠️ Important Medical Disclaimer</p>
          <p className="text-yellow-800">
            This screening tool is for informational purposes only and should not be considered a medical diagnosis. 
            The results are generated using a machine learning model trained on voice analysis data. 
            Always consult a qualified healthcare professional for proper medical evaluation, diagnosis, and treatment. 
            Early diagnosis and professional medical advice are crucial for managing health conditions.
          </p>
        </div>
      </div>
    </div>
  );
}

export default Result;
